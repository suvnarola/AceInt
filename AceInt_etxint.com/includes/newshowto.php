    <td>Licensees and nominated staff can load their newsletters directly to the 
    corporate website from this location. <br>
    <br>
    It is important that you convert your newsletter to PDF using Acrobat before 
    uploading. You will need to purchase the full version of Acrobat, or use one 
    of the free programmes downloadable from the web. This will assist in 
    keeping file sizes small, and allow all members to view the document. 
    Programmes such as Word and Publisher are proprietary and cannot be viewed 
    unless the reader has that same software and version.<br>
    <br>
    Newsletters can be uploaded by clicking on ‘Browse’ after the ‘File Upload’ 
    field and navigating to the location on the computer where the file is 
    stored. Assign a display name to the file which should be the month and year 
    of the publication, e.g. January 2004 in the case of monthly publications. 
    If more than one newsletter is created in a given month then the date 
    precedes the month and year e.g. 10 January 2004. <br>
    <br>
    Next, select the date of the newsletter in the ‘Order Date’ field. This will 
    ensure the newsletter links are displayed on the screen in the correct date 
    order, from the most recent to the oldest. The licensed area for which the 
    newsletter has been created must be selected, then the ‘Upload’ button is 
    pressed to confirm the upload of the newsletter file. <br>
    <br>
    You can also correct errors on previously uploaded files by first deleting 
    them, then reloading the correct file. Click on the space beside the file 
    name you wish to delete. The selected file will be highlighted by a dark 
    grey. You can select multiple files, or delete them individually. Once all 
    files you wish to delete have been selected, make the button beside ‘Delete 
    Selected Files’ at the bottom left of the page, then complete the process by 
    clicking on the ‘Delete’ button on the bottom right of the page.<br>
    <br>
    Now load the correct files. It is a good idea to retain past newsletters so 
    members can look back over services and specials that have been offered in 
    the past. This will also be a good marketing tool for prospective members.<br>
